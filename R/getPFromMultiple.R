#' takes a multiple linear regression model and calculates the p-value for the whole model
#'
#' @param lm model
#' @return numerical p-value
#' @export
getPFromMultiple <- function(model){
  rounding <- function(){3} ## I have a purpose here
  f <- summary(model)$f
  return(pf(f[1], f[2], f[3], lower.tail = FALSE))
}
